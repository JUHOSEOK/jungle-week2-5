# 트리 - 상근이의 여행 (백준 실버4)
# 문제 링크: https://www.acmicpc.net/problem/9372
#
# 1. 문제 한 줄 요약
# - 이 문제는 N개의 국가(정점)를 모두 방문할 수 있도록 연결할 때 필요한 최소 비행기 수를 구하는 문제다.
#
# 2. 함수 정의
# - 함수 이름: solve_testcase
# - 입력: 국가 수 N, 비행기 종류 수 M, 그리고 M개의 비행 연결 정보
# - 출력: 모든 국가를 방문하기 위해 필요한 최소 비행기 종류 수
# - 의미: 이 함수는 연결 그래프에서 모든 정점을 방문하기 위한 최소 간선 수를 반환한다.
#
# 3. 작은 예시
# - 예시 입력:
#   N = 3
#   간선: 1-2, 2-3, 1-3
#
# - 손으로 풀면:
#   1, 2, 3 세 나라를 모두 방문하려면
#   1-2, 2-3 만 있어도 전부 갈 수 있다.
#   1-3은 없어도 된다.
#   즉 2개면 충분하다.
#
# - 따라서 답은:
#   2 = N-1
#
# 4. 접근
# - 바로 구할 수 있나 / 더 작은 문제로 줄일 수 있나?
#   이 문제는 직접 그래프 탐색을 해도 되지만,
#   연결 그래프의 성질을 이용하면 바로 구할 수 있다.
#
# - 사용할 개념:
#   그래프, 스패닝 트리
#
# 5. 재귀라면
# - 이 문제는 재귀로 풀 필요는 없다.
# - 핵심은 연결 그래프의 최소 간선 수 성질을 이용하는 것이다.
#
# 6. 검증
# - 왜 맞는가:
#   모든 국가를 방문 가능하려면 모든 정점이 연결되어 있어야 한다.
#   연결된 N개의 정점을 사이클 없이 최소 간선으로 연결한 구조는 트리이다.
#   트리는 항상 간선이 N-1개이므로,
#   모든 국가를 방문하기 위한 최소 비행기 수는 N-1개이다.
#
# - 엣지 케이스:
#   N=2이면 답은 1
#   간선이 많아도(사이클이 많아도) 최소 연결만 남기면 항상 N-1개
#
# 7. 복잡도
# - 시간복잡도:
#   O(1)  (입력만 받고 답은 N-1 출력)
#   실제 구현에서는 입력 간선들을 읽어야 하므로 전체 입력 처리 기준 O(M)
#
# - 공간복잡도:
#   O(1)
result = []

T = int(input())
for i in range(T):
    N, M = list(map(int,input().split()))

    for _ in range(M):
        input()

    result.append(N-1)

for i in result:
    print(i)